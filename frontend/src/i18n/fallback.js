let currentLang = localStorage.getItem('i18nextLng') || 'en';

export const getTranslation = (key, params = {}) => {
  const keys = key.split('.');
  let value = translations[currentLang];
  for (const k of keys) {
    value = value?.[k];
  }
  if (!value || value === key) {
    if (currentLang !== 'en') {
      value = translations.en;
      for (const k of keys) {
        value = value?.[k];
      }
    }
  }
  if (!value || value === key) return key;
  
  // Simple parameter replacement
  if (params && typeof value === 'string') {
    return value.replace(/\{\{(\w+)\}\}/g, (match, paramKey) => {
      return params[paramKey] !== undefined ? params[paramKey] : match;
    });
  }
  
  return value || key;
};

const I18N_CHANGE_EVENT = 'i18n:languageChange';

export const setLanguage = (lang) => {
  currentLang = lang;
  localStorage.setItem('i18nextLng', lang);
  document.documentElement.setAttribute('dir', (lang === 'prs') ? 'rtl' : 'ltr');
  document.documentElement.setAttribute('lang', lang);
  if (typeof window !== 'undefined') {
    window.dispatchEvent(new CustomEvent(I18N_CHANGE_EVENT, { detail: { language: lang } }));
  }
};

// Initialize
setLanguage(currentLang);

// Simple hook-like function - subscribes to language changes so UI updates without refresh
export const useTranslation = () => {
  const [, setTick] = React.useState(0);
  React.useEffect(() => {
    const handler = () => setTick((n) => n + 1);
    window.addEventListener(I18N_CHANGE_EVENT, handler);
    return () => window.removeEventListener(I18N_CHANGE_EVENT, handler);
  }, []);
  // Stable reference: getTranslation reads currentLang at call time; language switches still
  // re-render via setTick. A new `t` every render breaks useCallback/useEffect deps (e.g. Dashboard fetch loop).
  const t = React.useCallback((key, params) => getTranslation(key, params), []);
  return {
    t,
    formatDate: formatDateUtil,
    formatTime: formatTimeUtil,
    formatDateLong: formatDateLongUtil,
    i18n: {
      language: currentLang,
      changeLanguage: setLanguage
    }
  };
};

// Default export for config.js compatibility
export default {
  get currentLang() { return currentLang; },
  getTranslation,
  setLanguage
};
